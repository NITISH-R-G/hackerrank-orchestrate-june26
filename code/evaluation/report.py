"""Generate evaluation_report.md from summary.json.

Reads evaluation/summary.json (produced by evaluation/main.py) and writes
evaluation/evaluation_report.md with all sections required by the problem
statement and specs/evaluation_spec.md §5-6.

Usage (from code/):  python -m evaluation.report
"""
import json
import sys
from pathlib import Path

HERE = Path(__file__).resolve().parent
SUMMARY_JSON = HERE / "summary.json"
REPORT_MD = HERE / "evaluation_report.md"

# Pricing assumptions (USD per 1M tokens) — gemini-2.5-flash, as of 2026.
PRICE_INPUT_PER_M = 0.30
PRICE_OUTPUT_PER_M = 2.50


def _fmt_pct(x):
    return f"{x*100:.1f}%"


def _confusion_table(cm):
    labels = ["supported", "contradicted", "not_enough_information"]
    short = {"supported": "supp", "contradicted": "contr",
             "not_enough_information": "nei"}
    lines = ["", "| true \\ pred | " + " | ".join(short[l] for l in labels) + " |",
             "|" + "---|" * (len(labels) + 1)]
    for t in labels:
        row = [short[t]] + [str(cm.get(t, {}).get(p, 0)) for p in labels]
        lines.append("| " + " | ".join(row) + " |")
    return "\n".join(lines)


def _build_report(summary: dict) -> str:
    out = []
    out.append("# Evaluation Report — Multimodal Damage Claim Verification\n")
    out.append("Generated by `evaluation/report.py` from `summary.json`.\n")

    winner = summary.get("winner", "unknown")
    limit = summary.get("limit")
    n_rows = summary.get("single", summary.get("two", {})).get("n_rows", "?")
    out.append(f"**Winner: Strategy {'A (single-pass)' if winner=='single' else 'B (two-pass)'}**  \n")
    out.append(f"**Evaluated on:** {n_rows} labeled rows"
               + (f" (limit={limit})" if limit else " (full sample)") + "\n\n")
    out.append("---\n")

    # --- Per-strategy comparison ---
    out.append("## 1. Strategy comparison\n")
    out.append("| Metric | Strategy A (single-pass) | Strategy B (two-pass) |")
    out.append("|---|---|---|")
    sa = summary.get("single", {})
    sb = summary.get("two", {})
    out.append(f"| Mean field accuracy | {_fmt_pct(sa.get('mean_accuracy',0))} | {_fmt_pct(sb.get('mean_accuracy',0))} |")
    out.append(f"| Overall exact-match (all 6 fields) | {_fmt_pct(sa.get('overall_exact_match',0))} | {_fmt_pct(sb.get('overall_exact_match',0))} |")
    out.append(f"| claim_status macro-F1 | {_fmt_pct(sa.get('claim_status_prf',{}).get('macro',{}).get('f1',0))} | {_fmt_pct(sb.get('claim_status_prf',{}).get('macro',{}).get('f1',0))} |")
    out.append(f"| Runtime (s) | {sa.get('runtime_seconds','?')} | {sb.get('runtime_seconds','?')} |")
    out.append(f"| Avg latency/row (s) | {sa.get('avg_latency_seconds','?')} | {sb.get('avg_latency_seconds','?')} |")
    out.append("")

    # --- Per-field accuracy table ---
    out.append("## 2. Per-field exact-match accuracy\n")
    fields = ["claim_status", "issue_type", "object_part", "severity",
              "valid_image", "evidence_standard_met"]
    out.append("| Field | Strategy A | Strategy B |")
    out.append("|---|---|---|")
    acc_a = sa.get("per_field_accuracy", {})
    acc_b = sb.get("per_field_accuracy", {})
    for f in fields:
        out.append(f"| {f} | {_fmt_pct(acc_a.get(f,0))} | {_fmt_pct(acc_b.get(f,0))} |")
    out.append(f"| **mean** | **{_fmt_pct(sa.get('mean_accuracy',0))}** | **{_fmt_pct(sb.get('mean_accuracy',0))}** |")
    out.append("")

    # --- Field-wise error counts ---
    out.append("## 3. Field-wise error counts (rows wrong)\n")
    ea = sa.get("field_error_counts", {})
    eb = sb.get("field_error_counts", {})
    out.append("| Field | Strategy A errors | Strategy B errors |")
    out.append("|---|---|---|")
    for f in fields:
        out.append(f"| {f} | {ea.get(f,'?')} | {eb.get(f,'?')} |")
    out.append("")

    # --- Confusion matrices ---
    for strat, name in (("single", "Strategy A (single-pass)"),
                        ("two", "Strategy B (two-pass)")):
        cm = summary.get(strat, {}).get("confusion_matrix", {})
        out.append(f"## 4. claim_status confusion — {name}\n")
        if cm:
            out.append(_confusion_table(cm))
            out.append("")
            prf = summary.get(strat, {}).get("claim_status_prf", {})
            out.append("\nPer-class precision/recall/F1:\n")
            out.append("| Class | Precision | Recall | F1 | Support |")
            out.append("|---|---|---|---|---|")
            for label in ("supported", "contradicted", "not_enough_information"):
                m = prf.get(label, {})
                out.append(f"| {label} | {_fmt_pct(m.get('precision',0))} | {_fmt_pct(m.get('recall',0))} | {_fmt_pct(m.get('f1',0))} | {m.get('support','?')} |")
            macro = prf.get("macro", {})
            out.append(f"| **macro** | **{_fmt_pct(macro.get('precision',0))}** | **{_fmt_pct(macro.get('recall',0))}** | **{_fmt_pct(macro.get('f1',0))}** | — |")
            out.append("")
        else:
            out.append("_(no data)_\n")

    # --- Winner rationale ---
    out.append("## 5. Winner selection\n")
    out.append("Selection rule (specs/evaluation_spec.md §4): higher mean field "
               "accuracy wins; ties go to Strategy B (two-pass) for determinism "
               "and targeted evidence.\n")
    out.append(f"\n**Selected: Strategy {'A (single-pass)' if winner=='single' else 'B (two-pass)'}** "
               f"(mean accuracy {_fmt_pct(summary.get(winner,{}).get('mean_accuracy',0))}).\n")
    out.append("The final `output.csv` for `dataset/claims.csv` is produced with "
               "this strategy only.\n")

    # --- Operational analysis ---
    out.append("## 6. Operational analysis\n")
    sa_st = sa.get("stats", {})
    sb_st = sb.get("stats", {})
    out.append("### Sample set (this evaluation)\n")
    for strat, name, st, rt in (("single", "Strategy A", sa_st, sa.get("runtime_seconds")),
                                ("two", "Strategy B", sb_st, sb.get("runtime_seconds"))):
        calls = st.get("pass2_calls", 0) + st.get("pass1_calls", 0)
        in_tok = st.get("input_tokens", 0)
        out_tok = st.get("output_tokens", 0)
        out.append(f"\n**{name}:** {calls} model calls "
                   f"(pass1={st.get('pass1_calls',0)}, pass2/single={st.get('pass2_calls',0)}, "
                   f"cache_hits={st.get('cache_hits',0)}, "
                   f"failures pass1={st.get('pass1_failures',0)} pass2={st.get('pass2_failures',0)}); "
                   f"~{in_tok} input + {out_tok} output tokens; "
                   f"runtime {rt}s.\n")
    out.append("\n### Projected full test set (`dataset/claims.csv`, 44 rows)\n")
    # project using the winner's per-row token usage
    win_st = summary.get(winner, {}).get("stats", {})
    win_rows = summary.get(winner, {}).get("n_rows", 1) or 1
    per_row_calls = (win_st.get("pass1_calls", 0) + win_st.get("pass2_calls", 0)) / win_rows
    per_row_in = win_st.get("input_tokens", 0) / win_rows
    per_row_out = win_st.get("output_tokens", 0) / win_rows
    test_calls = per_row_calls * 44
    test_in = per_row_in * 44
    test_out = per_row_out * 44
    cost = (test_in / 1_000_000) * PRICE_INPUT_PER_M + (test_out / 1_000_000) * PRICE_OUTPUT_PER_M
    out.append(f"- **Model calls (est):** ~{test_calls:.0f} (winner = "
               f"{'single-pass' if winner=='single' else 'two-pass'})\n")
    out.append(f"- **Tokens (est):** ~{test_in:.0f} input + ~{test_out:.0f} output\n")
    out.append("- **Images processed (sample):** 31; **(test):** 85\n")
    out.append(f"- **Cost (est, USD):** ~${cost:.3f} at "
               f"${PRICE_INPUT_PER_M}/M input, ${PRICE_OUTPUT_PER_M}/M output "
               f"(gemini-2.5-flash)\n")
    out.append(f"- **Latency (est):** ~{per_row_calls * 13:.0f}s/row throttled "
               "=> ~{:.0f} min for 44 rows\n".format(per_row_calls * 13 * 44 / 60))
    out.append("\n### Rate-limit / retry / caching strategy\n")
    out.append("- **Rate limit:** free tier is **5 RPM** for gemini-2.5-flash "
               "(confirmed by a 429 during dry-run). Enforced via `_throttle()` "
               "sleeping `SLEEP_BETWEEN_CALLS=13s` before every call, keeping us "
               "under 5 RPM.\n")
    out.append("- **Retry:** `tenacity`-style loop, MAX_RETRIES=5, exponential "
               "backoff (multiplier 2, min 13s, max 60s) on 429/5xx only; "
               "non-retryable (400/401) propagate immediately.\n")
    out.append("- **Caching:** deterministic on-disk cache keyed on "
               "(model, temperature, prompts, image SHA-256 hashes). Cache hit "
               "= 0 model calls, 0 tokens. Enables resume-after-crash (US-006) "
               "without reprocessing completed claims.\n")
    out.append("- **Batching:** none (sequential), because the 5 RPM ceiling "
               "makes concurrency counterproductive.\n")

    out.append("\n## 7. Recommendations for production deployment\n")
    out.append("- Move to a paid tier to raise the RPM ceiling; then add modest "
               "concurrency (2-3 workers) with a token-bucket limiter.\n")
    out.append("- Keep the deterministic normalization + consistency layers as "
               "the contract guarantor regardless of model.\n")
    out.append("- Add per-user caching of the Pass 1 intent extraction when the "
               "same conversation is re-submitted.\n")
    out.append("- Monitor `pass1_failures`/`pass2_failures`; sustained failures "
               "should page an operator (indicates model outage or quota).\n")
    out.append("\n## 8. Reproducibility\n")
    out.append("```bash\n# from code/\npython -m evaluation.main --full      # run both strategies\npython -m evaluation.report         # write this report\npython main.py                       # produce output.csv with the winner\n```\n")

    return "\n".join(out)


def main(argv=None) -> int:
    if not SUMMARY_JSON.exists():
        print(f"{SUMMARY_JSON} not found; run evaluation.main --full first.")
        return 1
    summary = json.loads(SUMMARY_JSON.read_text(encoding="utf-8"))
    REPORT_MD.write_text(_build_report(summary), encoding="utf-8")
    print(f"Wrote {REPORT_MD}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
